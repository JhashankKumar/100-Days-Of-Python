# Print Numbers
print(5)
print(10)
print(15)

# Print the result of a mathematical expression
print(5 + 10)
print(10 - 5)
print(5 * 10)
print(10 / 5)

# print a number with a string
print("The result of 5 + 10 is " + str(5 + 10))
print("The result of 10 - 5 is " + str(10 - 5))
print("The result of 5 * 10 is " + str(5 * 10))
print("The result of 10 / 5 is " + str(10 / 5))

# print a number with a string using f-string
print(f"The result of 5 + 10 is {5 + 10}")
print(f"The result of 10 - 5 is {10 - 5}")
print(f"The result of 5 * 10 is {5 * 10}")