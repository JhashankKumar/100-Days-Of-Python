# Text Output

# Double quotes can be used for strings
print("Hello, World!")
print("Python is a great programming language.")
print("I am learning Python.")

# Single quotes can also be used for strings
print('Hello, World!')
print('Python is a great programming language.')
print('I am learning Python.')

# Triple quotes can be used for multi-line strings
print("""Hello, World!
Python is a great programming language.
I am learning Python.""")

