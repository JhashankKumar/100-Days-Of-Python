# Statements are instructions that a Python interpreter can execute. 
# A statement is a logical instruction that the Python interpreter can execute. 
# A statement can be a single line of code or a block of code. 
# A statement can be an expression, a function call, a loop, a conditional statement, etc.

print("Hello, World!") # This is a statement
x = 5 # This is a statement
y = 10 # This is a statement
if x < y: # This is a statement
    print("x is less than y") # This is a statement
else: # This is a statement
    print("x is greater than or equal to y") # This is a statement  

# Semicolons can be used to separate multiple statements on the same line.
x = 5; y = 10; print(x + y) # This is a statement with multiple statements on the same line.

""" However, it is not recommended to use semicolons to separate statements on the same line, 
as it can make the code less readable. It is better to write each statement on a separate line."""