# Naming Rules
# 1. A variable name must start with a letter or the underscore character
# 2. A variable name cannot start with a number
# 3. A variable name can only contain alpha-numeric characters and underscores (A-z, 0-9, and _ )
# 4. Variable names are case-sensitive (age, Age and AGE are three different variables)
# 5. A variable name cannot be any of the Python keywords.

# Valid variable names
my_variable = 10
myVariable = 10
_my_variable = 10
my_variable_2 = 10 

# Invalid variable names
# 2my_variable = 10 # variable name cannot start with a number
# my-variable = 10 # variable name cannot contain a hyphen
# my variable = 10 # variable name cannot contain a space
# my.variable = 10 # variable name cannot contain a dot