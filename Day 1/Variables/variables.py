# variable creation
my_variable = 10
print(my_variable)

# variable assignment
x = 5
y = 10
z = x + y
print(z)

# variable reassignment
x = 5
print(x)
x = 10
print(x)

# reassigning a variable with a different data type
""" In Python, you can reassign a variable to a different data type. 
This is one of the features of Python that makes it a dynamically typed language."""
x = 5
print(x)
x = "Hello, World!"
print(x)

# casting a variable to a different data type
x = 5
print(x)
x = str(x)
print(x)

y = "10"
print(y)
y = int(y)
print(y)
y = float(y)
print(y)

