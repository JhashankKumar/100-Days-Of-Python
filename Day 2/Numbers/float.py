# Float
a = 10.5
print(a)  # Output: 10.5
print(type(a))  # Output: <class 'float'>

b = -3.14
print(b)  # Output: -3.14
print(type(b))  # Output: <class 'float'>

# Float literals
b = 3.14
c = 2.0
d = 1e10  # Scientific notation (10^10)
print(b)  # Output: 3.14
print(c)  # Output: 2.0
print(d)  # Output: 10000000000.0

e = -87.7e100
print(e)  # Output: -8.77e+101
"""Float literals can also be expressed in scientific notation, 
where 'e' or 'E' is used to indicate the power of 10. 
For example, 1e10 represents 1 multiplied by 10 to the power of 10, which is 10000000000.0. 
Similarly, -87.7e100 represents -87.7 multiplied by 10 to the power of 100, which is -8.77e+101."""


