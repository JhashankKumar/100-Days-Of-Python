# Integer
a = 10
print(a)  # Output: 10
print(type(a))  # Output: <class 'int'>

b = -5
print(b)  # Output: -5
print(type(b))  # Output: <class 'int'>

# Integer literals in different bases
binary_num = 0b1010  # Binary literal (10 in decimal)
octal_num = 0o12     # Octal literal (10 in decimal)
hexadecimal_num = 0xA  # Hexadecimal literal (10 in decimal)
print(binary_num)  # Output: 10
print(octal_num)   # Output: 10
print(hexadecimal_num)  # Output: 10 

