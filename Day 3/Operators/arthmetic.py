# Arithmetic Operators
# +, -, *, /, //, %, **

# Addition
print(10 + 5)  # Output: 15

# Subtraction
print(10 - 5)  # Output: 5

# Multiplication
print(10 * 5)  # Output: 50

# Division
print(10 / 5)  # Output: 2.0

# Floor Division
print(10 // 5)  # Output: 2

# Modulus
print(10 % 5)  # Output: 0

# Exponentiation
print(10 ** 5)  # Output: 100000

