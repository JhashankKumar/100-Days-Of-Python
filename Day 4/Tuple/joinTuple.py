# join tuple using the + operator
tuple1 = (1, 2, 3)
tuple2 = (4, 5, 6)
joined_tuple = tuple1 + tuple2  
print(joined_tuple)  # Output: (1, 2, 3, 4, 5, 6)

# join tuple using the * operator
tuple1 = (1, 2, 3)
joined_tuple = tuple1 * 2
print(joined_tuple)  # Output: (1, 2, 3, 1, 2, 3)

triple_tuple = (1, 2, 3)
joined_tuple = triple_tuple * 3
print(joined_tuple)  # Output: (1, 2, 3, 1, 2, 3, 1, 2, 3)  

